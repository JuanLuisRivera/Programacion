#include<stdio.h>

int main(void)
{
    printf("Podrá nublarse el sol eternamente;\n");
    printf("Podrá secarse en un instante el mar;\n");
    printf("Podrá romperse el eje de la tierra;\n");
    printf("Como un débil cristal.\n");
    printf("¡Todo sucederá! Podrá la muerte\n");
    printf("Cubrirme con su fúnebre crespón;\n");
    printf("Pero jamás en mí podrá apagarse\n");
    printf("La llama de tu amor.\n");
    return 0;
}